## CEIL FUNCTION ()

import math

total_sales = 20000.60

print(math.ceil(total_sales)) ## math.ceil << will round off the value to its highest order

print(math.floor(total_sales)) ## math.floor << will round off the value to its lowest order

total_sales_2 = -20000.60

print(math.fabs(total_sales_2)) ## math.fabs << gives the absolute value ( remove sign )
